public class loop {

    public static void main(String[] args) {
// create a while loop that returns back the multiple of 3 and 5 up until 100
        int i = 0;
        while (i <= 100) {
            if (i % 3 == 0 && i % 5 == 0) {
                System.out.println(i);
            }
            i++;


        }




    }
}